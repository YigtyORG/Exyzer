using System;

namespace Exyzer.Apps.Shell
{
	internal static class Program
	{
		[STAThread()]
		private static int Main(string[] args)
		{
			try {

				return 0;
			} catch (Exception e) {
				Console.ForegroundColor = ConsoleColor.Red;
				Console.Error.WriteLine();
				Console.Error.WriteLine(e.ToString());
				Console.ResetColor();
				return e.HResult;
			}
		}
	}
}
